package com.aw.tutorial;

public class VariableAssignment {
        public static void main(String[] args) {
            // Declare and assign values to variables
            String name = "Monica";
            int age = 20;
            double marks = 87.5;
            boolean isPassed = true;
    
            // Print the variables
            System.out.println("Name: " + name);
            System.out.println("Age: " + age);
            System.out.println("Marks: " + marks);
            System.out.println("Passed: " + isPassed);


            int counter = 1; // Initialize counter to 1
        System.out.println("Counter value: " + counter);

        counter ++; //  increase value to 2
        System.out.println("Counter value after update: " + counter);


        final double PI = 3.14; // Declare a final variable
        System.out.println("Original value of PI: " + PI);

        // Attempt to update PI
        PI = 3.14159; 
        System.out.println("Updated value of PI: " + PI); 

        }


        }
    

